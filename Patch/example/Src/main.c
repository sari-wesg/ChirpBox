/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_CRC_Init();

  /* Patch --------------------------------------------------------------------*/

  // Prepare for download files
  menu_preSend();
  // download source file at page 10
  uint32_t original_size = menu_serialDownload(10);
  // download patch file at page 15
  uint32_t patch_size = menu_serialDownload(15);
  printf("size: %lu, %lu\n", original_size, patch_size);
  // patch the new file at page 20
  if (!Filepatch(10, original_size, 15, patch_size, 20))
    printf("Patch success!\n");
  else printf("Patch failed!\n");
}

